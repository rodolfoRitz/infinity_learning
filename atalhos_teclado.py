'''Atalhos para ABRIR'''
# ALT + "número da aba que deseja abrir" : abre a aba selecionada.
# CNTL + SHIFT + N : abre uma nova aba independente para novos arquivos.
# Ctrl + Shift + T : para reabrir a última aba fechada.
# CNTRL + K + W : fecha a aba selecionada.
# CNTRL + J : seleciona/abre o terminal.
# CNTRL + K + O : abre todos os arquivos da pasta selecionada.

'''Atalhos para FECHAR'''
# CNTRL + W : fecha a aba atual.
# CNTRL + ' : fecha o terminal.
# Ctrl + Shift + K : para excluir uma linha inteira.
# Use Ctrl+K Ctrl+W : para fechar todas as abas abertas.

'''Atalhos para CRIAR'''
# CNTL + N : cria um novo arquivo.
# CNTRL + S : salva o arquivo.
# CNTL + O : abre o arquivo selecionado.
# CNTL + SHIFT + T : abre o último arquivo que foi fechado.
# Ctrl + H : para substituir uma palavra no código.

'''Atalhos para PESQUISAR'''
# CNTL + P : pesquisa pelo nome do arquivo.
# CNTL + SHIFT + O (pesquisa com @) : pelo nome da função.
# CNTRL + T (pesquisa com #) : para procurar por uma função ou variável específica.
# CNTRL + G + número da linha: pesquisa pela linha do código informada.
# Ctrl + F: para encontrar todas as instâncias de uma palavra no código.

'''Atalhos para SELECIONAR'''
# ALT + SHIFT + setinha pra cima ou pra baixo: duplica a linha na direção da setinha.
# ALT + seta pra cima ou pra baixo, para mover a linha atual para cima ou para baixo.
'''CNTRL+D : seleciona a palavra que está em cima do cursor do teclado.                  AO CLICAR NOVAMENTE 'D' VAI PARA A PRÓXIMA PALAVRA SELECIONADA !!'''
# CNTL + SHIFT + setinha pro lado : vai selecionando o texto da linha.
# CNTL + SHIFT + L : para selecionar todas as ocorrências de uma palavra específica e substituí-las simultaneamente.
# Ctrl + Alt + Seta para Cima/Baixo : para adicionar múltiplos cursores acima ou abaixo do cursor atual.

'''Atalhos do VS Code'''
# CNTL + SHIFT + P : abre uma janela para editar a configuração de tudo do VS Code.
# CNTL + K + T : muda o tema das cores do VS Code.
# CNTRL + ALT + SETINHA : Divide as janelas dentro do VS Code.
# Alt + Click : para colocar cursores em várias linhas ao mesmo tempo.

